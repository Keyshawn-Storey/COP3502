class Cow {

    private String name;
    private String image;

    public Cow(String name) {
        this.name = name;
    }
    // Gets the name
    public String getName() {
        return name;
    }
    // Gets the image
    public String getImage() {
        return image;
    }
    // Sets the image
    public void setImage(String _image) {
        image = _image;
    }
}


